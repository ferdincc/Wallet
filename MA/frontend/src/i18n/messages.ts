/** App UI is English-only (`messages.en`). */
export type Locale = 'en'

const STORAGE_LOCALE = 'okyiss_locale'

export function readStoredLocale(): Locale {
  try {
    const v = localStorage.getItem(STORAGE_LOCALE)
    if (v === 'tr') localStorage.removeItem(STORAGE_LOCALE)
  } catch {
    /* ignore */
  }
  return 'en'
}

